// String: A string is text or anything written inside of quotes(single or double quotes)
var person = "John Doe";
console.log(typeof person);

// number: Number are not written inside of quotes
var age = 50;
console.log(typeof age);

// boolean: True or False
var isClassGoingOn = false;
console.log(typeof isClassGoingOn);

// undefined: A variable that is not assigned any value evaluates to undefined. You can also expressly set a variable to defined.

// var blah = undefined;
var blah;
console.log(typeof blah);

// null: empty
var X = null;
console.log(typeof X); 
//typeof null in Javascript evaluates to object. This is actually a bug in javasript. Null is not an object. Null is empty

