// *=
let num4 = 15;
num4 *= 2; //multiply 15 by 2
console.log(num4); // 30

// /=
let num5 = 30;
num5 /= 2; // divide 30 by 2
console.log(num5); //15

// %=
let num6 = 5;
num6 %= 2; // gets the remainder of the division 5/2
console.log(num6);